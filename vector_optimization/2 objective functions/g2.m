function g2=g2(x,d)
if d==1% BK1
        g2=[2*x(1)-10;2*x(2)-10];
        
elseif d==2% DGO1
        g2=cos(x+0.7);
        
elseif d==3% DGO2
        g2=x/sqrt(81-x^2);
        
elseif d==4% FF1
 
g21=2*(x(1)+1)*exp(-(x(1)+1)^2-(x(2)-1)^2);
g22=2*(x(2)-1)*exp(-(x(1)+1)^2-(x(2)-1)^2);
g2=[g21;g22];

elseif d==5%  JOS1
 
n=length(x);
a=x(1:n);
g2=(2/n)*(a-2);

elseif d==6%  JOS1
 
n=length(x);
a=x(1:n);
g2=(2/n)*(a-2);


elseif d==7%  JOS1
 
n=length(x);
a=x(1:n);
g2=(2/n)*(a-2);

elseif d==8%  MLF1
 
g2=cos(x)/20-(1+x/20)*sin(x);
elseif d==9%  MLF2
 
g21=(4*x(1)*(4*x(1)^2+2*x(2)-11)+(2*x(1)+4*x(2)^2-7))/50;
g22=((4*x(1)^2+2*x(2)-11)+4*x(2)*(2*x(1)+4*x(2)^2-7))/50;
g2=[g21;g22];

elseif d==10%  TE1
 
g2=[2*x(1);x(2)-10];

elseif d==11%  TE2
 
g2=[2*(x(1)-1/sqrt(2))*exp(-(x(1)-1/sqrt(2))^2-(x(2)-1/sqrt(2))^2);2*(x(2)-1/sqrt(2))*exp(-(x(1)-1/sqrt(2))^2-(x(2)-1/sqrt(2))^2)];

elseif d==12%  TE4
 
n=length(x);
g2=ones(n,1);

elseif d==13%  TE6
 
g2=[2*x(1);1];

elseif d==14%  TE7
 
n=length(x);
g2=ones(n,1);

elseif d==15%  SP1
 
g21=2*(x(1)-x(2));
g22=2*(x(2)-3)-2*(x(1)-x(2));
g2=[g21;g22];

elseif d==16%  SSFYY2
 
g2=2*(x-4);

elseif d==17%  SK1
 
g2=-(-2*x^3+6*x^2+20*x-10);
elseif d==18% SK2
 
b=x(1)^2+x(2)^2+x(3)^2+x(4)^2;
c=sin(x(1))+sin(x(2))+sin(x(3))+sin(x(4));
g21=-((cos(x(1))*(1+b/100)-c*x(1)/50)/(1+b/100)^2);
g22=-((cos(x(2))*(1+b/100)-c*x(2)/50)/(1+b/100)^2);
g23=-((cos(x(3))*(1+b/100)-c*x(3)/50)/(1+b/100)^2);
g24=-((cos(x(4))*(1+b/100)-c*x(4)/50)/(1+b/100)^2);
g2=[g21;g22;g23;g24];
elseif d==19%  VU1
 
g21=2*x(1);g22=6*x(2);
g2=[g21;g22];
elseif d==20%  MOP1
 
g2=2*(x-2);

elseif d==21%  MOP2
 
n=length(x);
a=x(1:n);
f2=0;
for i=1:n
    b=(x(i)+1/sqrt(n))^2;
    f2=f2+b;
end
g2=2*(a+1/sqrt(n))*exp(-f2);

elseif d==22%  MOP3
 
g21=2*(x(1)+3);
g22=2*(x(2)+1);
g2=[g21;g22];

elseif d==23%  DD1
 
g2=[3;2;-1/3;0.03*(x(4)-x(5))^2;-0.03*(x(4)-x(5))^2];

elseif d==24 %Toi4
g2=[x(1)-x(2);-(x(1)-x(2));x(3)-x(4);-(x(3)-x(4))];

elseif d==25%  Hil1
a=(2*pi/360)*(45+40*sin(2*pi*x(1))+25*sin(2*pi*x(2)));
a1=((2*pi)/360)*80*pi*cos(2*pi*x(1));
a2=((2*pi)/360)*50*pi*cos(2*pi*x(2));
b=1+0.5*cos(2*pi*x(1));
b1=-pi*sin(2*pi*x(1));
c1=sin(a);c2=cos(a);
g2=[c2*a1*b+c1*b1;c2*a2*b];

elseif d==26%  PNR
 
k=1;l=0;
g2=[2*(x(1)-k);2*(x(2)-l)];

elseif d==27% MMR1
 
 g21=(2*x(1)*((4*exp(-((5*x(2))/2 - 3/2)^2))/5 + exp(-(25*x(2)-5)^2) - 2))/(x(1)^2 + 1)^2;
 g22= ((4*exp(-((5*x(2))/2 - 3/2)^2)*((25*x(2))/2 - 15/2))/5 + exp(-(25*x(2) - 5)^2)*(1250*x(2) - 250))/(x(1)^2 + 1);
 g2=[g21;g22];

elseif d==28 %AP3
    g2=[2*(x(2)-x(1)^2)*(-2*x(1))-2*(1-x(1));2*(x(2)-x(1)^2)];

elseif d==29  %Lov5
     M=[-1,-0.03,0.011;-0.03,-1,0.07;0.011,0.07,-1.01];
     ff1=sqrt(2*pi/0.35)*exp(([x(1);x(2)-0.15;x(3)]'*M*[x(1);x(2)-0.15;x(3)])/0.35^2);
     ff2=sqrt(2*pi/3)*exp(([x(1);x(2)+1.1;0.5*x(3)]'*M*[x(1);x(2)+1.1;0.5*x(3)])/3^2);
     g11=-sqrt(2)/2+sqrt(2)/2*ff1*(-x(1)-0.03*x(2)+0.0045+0.011*x(3)-x(1)-0.03*(x(2)-0.15)+0.011*x(3))/0.35^2+sqrt(2)/2*ff2*(-x(1)-0.03*x(2)-0.033+0.011*x(3)/2-0.03*(x(2)+1.1)+0.011*x(3)/2)/3^2;
     g12=sqrt(2)/2*ff1*(-0.03*x(1)-1*(x(2)-0.15)-0.03*x(1)-x(2)+0.15+0.07*x(3)+0.07*x(3))/0.35^2+sqrt(2)/2*ff2*(-0.03*x(1)-1*(x(2)+1.1)-0.03*x(1)-x(2)-1.1+0.07*x(3)/2+0.07*x(3)/2)/3^2;
     g13=sqrt(2)/2*ff1*(0.011*x(1)+0.07*(x(2)-0.15)-1.01*x(3)+0.011*x(1)+0.07*x(2)-0.0105-x(3)*1.01)/0.35^2+sqrt(2)/2*ff2*(0.011*x(1)+0.07/2*(x(2)+1.1)-1.01/2*(x(3)/2)+0.011*x(1)+0.07*x(2)+0.07-1.01*x(3)/2*0.5)/3^2;
     g2=[g11;g12;g13];
end
