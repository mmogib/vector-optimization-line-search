function g1=g1(x,d)
if d==1 % BK1
        g1=[2*x(1);2*x(2)];
        
elseif d==2% DGO1
        g1=cos(x);
        
elseif d==3% DGO2
        g1=2*x;

elseif d==4% FF1
 
g11=2*(x(1)-1)*exp(-(x(1)-1)^2-(x(2)+1)^2);
g12=2*(x(2)+1)*exp(-(x(1)-1)^2-(x(2)+1)^2);
g1=[g11;g12];

elseif d==5%%  JOS1
 
n=length(x);
a=x(1:n);
g1=(2/n)*a;

elseif d==6%  JOS1
 
n=length(x);
a=x(1:n);
g1=(2/n)*a;

elseif d==7%  JOS1
n=length(x);
a=x(1:n);
g1=(2/n)*a;

elseif d==8%  MLF1
 
g1=sin(x)/20+(1+x/20)*cos(x);

elseif d==9%  MLF2
 
g11=(2*x(1)*(x(1)^2+x(2)-11)+(x(1)+x(2)^2-7))/100;
g12=((x(1)^2+x(2)-11)+2*x(2)*(x(1)+x(2)^2-7))/100;
g1=[g11;g12];

elseif d==10%  TE1
 
g1=[x(1)-10;2*x(2)];

elseif d==11%  TE2
 
g1=[0;cos(x(2))];

elseif d==12%  TE4
 
n=length(x);
a=x(1:n-1);
g11=2*a;
g1=[g11;0];

elseif d==13%  TE6
 
g1=[-1/x(1);-1/x(2)];

elseif d==14%  TE7
 
n=length(x);
a=x(1:n);
g1=4*a.^3+3*a.^2;

elseif d==15%  SP1
 
g12=-2*(x(1)-x(2));
g11=2*(x(1)-1)+2*(x(1)-x(2));
g1=[g11;g12];

elseif d==16%  SSFYY2
 g1=2*x+5*pi*sin(pi*x/2);  
 
elseif d==17%  SK1
g1=-(-4*x^3-9*x^2+20*x+10);

elseif d==18% SK2
 
g1=[2*(x(1)-2);2*(x(2)+3);2*(x(3)-5);2*(x(4)-4)];
elseif d==19%  VU1
 
g11=(-2*x(1))/(x(1)^2+x(2)^2+1)^2;
g12=(-2*x(2))/(x(1)^2+x(2)^2+1)^2;
g1=[g11;g12];
elseif d==20%  MOP1
 
g1=2*x;

elseif d==21%  MOP2
 
n=length(x);
a=x(1:n);
f1=0;
for i=1:n 
    c=(x(i)-1/sqrt(n))^2;
    f1=f1+c;
end
g1=2*(a-1/sqrt(n))*exp(-f1);    
elseif d==22%  MOP3
 
A1=0.5*sin(1)-2*cos(1)+sin(2)-1.5*cos(2);
A2=1.5*sin(1)-cos(1)+2*sin(2)-0.5*cos(2);
B1=0.5*sin(x(1))-2*cos(x(1))+sin(x(2))-1.5*cos(x(2));
B2=1.5*sin(x(1))-cos(x(1))+2*sin(x(2))-0.5*cos(x(2));
g11=-(2*(A1-B1)*(0.5*cos(x(1))+2*sin(x(1)))+2*(A2-B2)*(1.5*cos(x(1))+sin(x(1))));
g12=-(2*(A1-B1)*(cos(x(2))+1.5*sin(x(2)))+2*(A2-B2)*(2*cos(x(2))+0.5*sin(x(2))));
g1=[g11;g12];

elseif d==23%  DD1
 
g1=[2*x(1);2*x(2);2*x(3);2*x(4);2*x(5)];

elseif d==24%  Toi4
 
g1=[2*x(1);2*x(2);0;0];

elseif d==25%  Hill
 
a=(2*pi/360)*(45+40*sin(2*pi*x(1))+25*sin(2*pi*x(2)));
a1=((2*pi)/360)*80*pi*cos(2*pi*x(1));
a2=((2*pi)/360)*50*pi*cos(2*pi*x(2));
b=1+0.5*cos(2*pi*x(1));
b1=-pi*sin(2*pi*x(1));
c1=sin(a);c2=cos(a);
g1=[-c1*a1*b+c2*b1;-c1*a2*b];

elseif d==26%  PNR
 
c=10;d=0.25;
g1=[4*x(1)^3+2*x(1)+c*x(2)+d;4*x(2)^3+2*x(2)+c*x(1)];

elseif d==27% MMR1
 
    g1=[2*x(1);0];

elseif d==28 %AP3
    g1=[(x(1)-1)^3;2*(x(2)-2)^3];
    elseif d==37
        g1=[-1;0];

 elseif d==29  %Lov5
     M=[-1,-0.03,0.011;-0.03,-1,0.07;0.011,0.07,-1.01];
     ff1=sqrt(2*pi/0.35)*exp(([x(1);x(2)-0.15;x(3)]'*M*[x(1);x(2)-0.15;x(3)])/0.35^2);
     ff2=sqrt(2*pi/3)*exp(([x(1);x(2)+1.1;0.5*x(3)]'*M*[x(1);x(2)+1.1;0.5*x(3)])/3^2);
     g11=sqrt(2)/2+sqrt(2)/2*ff1*(-x(1)-0.03*x(2)+0.0045+0.011*x(3)-x(1)-0.03*(x(2)-0.15)+0.011*x(3))/0.35^2+sqrt(2)/2*ff2*(-x(1)-0.03*x(2)-0.033+0.011*x(3)/2-0.03*(x(2)+1.1)+0.011*x(3)/2)/3^2;
     g12=sqrt(2)/2*ff1*(-0.03*x(1)-1*(x(2)-0.15)-0.03*x(1)-x(2)+0.15+0.07*x(3)+0.07*x(3))/0.35^2+sqrt(2)/2*ff2*(-0.03*x(1)-1*(x(2)+1.1)-0.03*x(1)-x(2)-1.1+0.07*x(3)/2+0.07*x(3)/2)/3^2;
     g13=sqrt(2)/2*ff1*(0.011*x(1)+0.07*(x(2)-0.15)-1.01*x(3)+0.011*x(1)+0.07*x(2)-0.0105-x(3)*1.01)/0.35^2+sqrt(2)/2*ff2*(0.011*x(1)+0.07/2*(x(2)+1.1)-1.01/2*(x(3)/2)+0.011*x(1)+0.07*x(2)+0.07-1.01*x(3)/2*0.5)/3^2;
     g1=[g11;g12;g13];
end